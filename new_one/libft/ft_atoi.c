#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int		sign;
	int		outcome;
	size_t	i;

	i = 0;
	outcome = 0;
	sign = 1;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		outcome = outcome * 10 + nptr[i] - '0';
		i++;
	}
	return (outcome * sign);
}
